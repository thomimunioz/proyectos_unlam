function agregarPeliculas(){
    let nodo_peliculas = document.querySelector("#divPeliculas");

    for (let peliculas of PELICULAS) {
        let nodo_a = document.createElement("a");
        nodo_a.href = `./peliculasDetails.html?nombre=${peliculas['nombre']}`;
        let nodo_img = document.createElement("img");
        nodo_img.src = peliculas.imagen;
        nodo_img.style.width = "462px";
        nodo_img.style.height = "200px";
        nodo_img.classList.add("carousel-cell-movies");
        nodo_a.appendChild(nodo_img);
        nodo_peliculas.appendChild(nodo_a);
    }
    reiniciarFlickityMovies();
}

function reiniciarFlickityMovies(){
    const carouselMovies = document.querySelector(".carousel-movies");

    if(carouselMovies && carouselMovies.classList.contains('flickity-enabled')) {
        carouselMovies.flickity('destroy');
    }

    const flktyMovies = new Flickity(carouselMovies, {
        cellAlign: 'left',
        contain: true,
        pageDots: false,
        wrapAround: true
    });
}

document.addEventListener('DOMContentLoaded', function(){
    agregarPeliculas();
});

localStorage.setItem('peliculas', JSON.stringify(PELICULAS));