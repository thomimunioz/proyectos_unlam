function mostrarEntrevistas(){
    let nodoEntrevistas = document.getElementById('entrevistasMostrar')

    for(let entrevistas of ENTREVISTAS){
        let nodoA = document.createElement('a')
        nodoA.href = `./entrevistasDetails.html?nombre=${entrevistas['nombre']}`
        nodoA.target= '_blank'
        let nodoIMG = document.createElement('img')
        nodoIMG.src = entrevistas.imagen
        nodoA.appendChild(nodoIMG)
        nodoEntrevistas.appendChild(nodoA)

    }
}

mostrarEntrevistas()
localStorage.setItem('entrevistas',JSON.stringify(ENTREVISTAS))

